# Enunciado:
# Faça um programa que leia um número Inteiro qualquer e mostre na tela a sua tabuada.

# Solução:


# Recebe o número intero
n=int(input('digite o número desejado '))

print('A tabuada de {n} é a seguinte:')
print(f'{n} X 01 é {n*1}')
print(f'{n} X 02 é {n*2}')
print(f'{n} X 03 é {n*3}')
print(f'{n} X 04 é {n*4}')
print(f'{n} X 05 é {n*5}')
print(f'{n} X 06 é {n*6}')
print(f'{n} X 07 é {n*7}')
print(f'{n} X 08 é {n*8}')
print(f'{n} X 09 é {n*9}')
print(f'{n} X 10 é {n*10}')
